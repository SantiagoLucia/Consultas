with reparticiones as (
Select 
T.CODIGO_TRATA,
TR.CODIGOREPARTICION CON_CARACTERES_ESPECIALES,
DUMP(TR.CODIGOREPARTICION) DUMP1,
REGEXP_REPLACE(TR.CODIGOREPARTICION,'[^A-Z0-9-]','') SIN_CARACTERES_ESPECIALES,
DUMP(REGEXP_REPLACE(TR.CODIGOREPARTICION,'[^A-Z0-9-]','')) DUMP2
FROM 
    EE_GED.TRATA T
    INNER JOIN EE_GED.TRATA_REPARTICION TR ON T.ID = TR.ID_TRATA
where TR.HABILITACION = 1 and T.ESTADO = 'ALTA'
)
select * from reparticiones where DUMP1 != DUMP2
ORDER BY CODIGO_TRATA;