--INFORMACION REFERIDA AL REGISTRO Y ACTA DE REGISTRO--
FROM RCE_GED.RCE_REGISTRO RR
LEFT JOIN RCE_GED.RCE_ACTA_REGISTRO RAR ON (RR.ID_REGISTRO = RAR.FK_REGISTRO)
LEFT JOIN RCE_GED.RCE_DIGITAL D ON (RR.ID_REGISTRO = D.ID_REGISTRO)
LEFT JOIN RCE_GED.RCE_DATO_TOPOGRAFICO DT ON (D.FK_DATO_TOPOGRAFICO = DT.ID_DATO_TOPOGRAFICO)
LEFT JOIN RCE_GED.RCE_VINCULACION RV ON RR.ID_REGISTRO = RV.ID_REGISTRO_SUCESOR

--INFORMACION REFERIDA AL SUBTIPO DE REGISTRO Y CONDICION--
LEFT JOIN RCE_GED.SYS_SUBTIPO_REGISTRO SSR ON (RR.FK_SUBTIPO_REGISTRO = SSR.ID_SUBTIPO_REGISTRO)
LEFT JOIN RCE_GED.RCE_CONDICION_REGISTRO CR1 ON CR1.FK_REGISTRO = RR.ID_REGISTRO AND CR1.FK_TIPO_CONDICION = 420
LEFT JOIN RCE_GED.RCE_CONDICION_REGISTRO CR2 ON CR2.FK_REGISTRO = RR.ID_REGISTRO AND CR2.FK_TIPO_CONDICION IN (428,430,432)
LEFT JOIN RCE_GED.RCE_CONDICION_REGISTRO CR3 ON CR3.FK_REGISTRO = RR.ID_REGISTRO AND CR3.FK_TIPO_CONDICION IN (429 , 431, 433)
LEFT JOIN RCE_GED.RCE_CONDICION_REGISTRO CR4 ON CR4.FK_REGISTRO = RR.ID_REGISTRO AND CR4.FK_TIPO_CONDICION IN (434, 435)
LEFT JOIN RCE_GED.RCE_CONDICION_REGISTRO CR5 ON CR5.FK_REGISTRO = RR.ID_REGISTRO AND CR5.FK_TIPO_CONDICION IN (436, 437, 440)
LEFT JOIN RCE_GED.RCE_CONDICION_REGISTRO CR6 ON CR6.FK_REGISTRO = RR.ID_REGISTRO AND CR6.FK_TIPO_CONDICION = 438
LEFT JOIN RCE_GED.RCE_CONDICION_REGISTRO CR7 ON CR7.FK_REGISTRO = RR.ID_REGISTRO AND CR7.FK_TIPO_CONDICION = 439
LEFT JOIN RCE_GED.JBPM4_TASK F ON RR.ID_FLUJO_JBPM = F.EXECUTION_ID_

--INFORMACION REFERIDA A LA CIRCUNSCRIPCION--
LEFT JOIN RCE_GED.SYS_CIRCUNSCRIPCION SC ON RR.FK_ID_CIRCUNSCRIPCION = SC.ID_CIRCUNSCRIPCION
LEFT JOIN RCE_GED.SYS_CIRCUNSCRIPCION SC ON DT.FK_CIRCUNSCRIPCION = SC.ID_CIRCUNSCRIPCION
LEFT JOIN EU_GED.EU_LOCALIDAD EL ON SC.FK_LOCALIDAD = EL.ID
LEFT JOIN EU_GED.EU_DEPARTAMENTO ED ON EL.ID_DEPARTAMENTO = ED.ID

--INFORMACION REFERIDA AL DIFUNTO--
LEFT JOIN RCE_GED.RCE_REGISTRO_PERSONA RRP ON (RR.ID_REGISTRO = RRP.FK_REGISTRO)
LEFT JOIN RCE_GED.RCE_PERSONA RP ON (RRP.FK_PERSONA = RP.ID_PERSONA)
LEFT JOIN RCE_GED.SYS_TIPO_DOCUMENTO STD ON (RP.FK_TIPO_DOCUMENTO = STD.ID_TIPO_DOCUMENTO)
LEFT JOIN RCE_GED.SYS_NACIONALIDAD NACPM ON NACPM.ID_NACIONALIDAD = RP.FK_NACIONALIDAD
LEFT JOIN RCE_GED.SYS_SEXO SEXPM ON SEXPM.ID_SEXO = RP.FK_SEXO
LEFT JOIN RCE_GED.RCE_DOMICILIO DOMPM ON DOMPM.ID_DOMICILIO = RP.FK_DOMICILIO
LEFT JOIN RCE_GED.SYS_ASENTAMIENTO ASEPM ON ASEPM.ID_ASENTAMIENTO = DOMPM.FK_ASENTAMIENTO

--INFORMACION REFERIDA A CAUSA DE DEFUNCION--
LEFT JOIN RCE_GED.RCE_DEFUNCION RD ON (RR.ID_REGISTRO = RD.ID_REGISTRO)
LEFT JOIN RCE_GED.SYS_CLASIFICACION_ENFERMEDADES ENF1 ON (ENF1.ID_CLASIFICACION_ENFERMEDADES = RD.FK_CAUSA_ORIGINARIA)
LEFT JOIN RCE_GED.SYS_CLASIFICACION_ENFERMEDADES ENF2 ON (ENF2.ID_CLASIFICACION_ENFERMEDADES = RD.FK_CAUSA_INMEDIATA)
LEFT JOIN RCE_GED.SYS_CLASIFICACION_ENFERMEDADES ENF3 ON (ENF3.ID_CLASIFICACION_ENFERMEDADES = RD.FK_CAUSA_MEDIATA)
LEFT JOIN RCE_GED.RCE_OBSERVACIONES OBS ON RD.FK_OTRA_CAUSA_INMEDIATA = OBS.ID_OBSERVACIONES
LEFT JOIN RCE_GED.RCE_OBSERVACIONES OBS1 ON OBS1.ID_OBSERVACIONES = RD.FK_OBSERVACION_LUGAR_DEFUNCION

--INFORMACION REFERIDA AL LUGAR DE DEFUNCION--

LEFT JOIN RCE_GED.RCE_DOMICILIO DOMICILIO_DEFUNCION ON RD.FK_DOMICILIO = DOMICILIO_DEFUNCION.ID_DOMICILIO
LEFT JOIN EU_GED.EU_PROVINCIA PROV_DOM_DEFU ON PROV_DOM_DEFU.ID = DOMICILIO_DEFUNCION.FK_PROVINCIA
LEFT JOIN RCE_GED.SYS_NACIONALIDAD PAIS_DOM_DEFU ON PAIS_DOM_DEFU.ID_NACIONALIDAD = DOMICILIO_DEFUNCION.FK_PAIS

--INFORMACION REFERIDA A MADRE/PADRE 1--
LEFT JOIN RCE_GED.RCE_REGISTRO_PERSONA RPM1 ON RPM1.FK_REGISTRO = RR.ID_REGISTRO AND RPM1.EVENTO = 'onPadre1'
LEFT JOIN RCE_GED.RCE_PERSONA RP1 ON (RPM1.FK_PERSONA = RP1.ID_PERSONA)
LEFT JOIN RCE_GED.SYS_TIPO_DOCUMENTO STD1 ON (RP1.FK_TIPO_DOCUMENTO = STD1.ID_TIPO_DOCUMENTO)
LEFT JOIN RCE_GED.SYS_NACIONALIDAD NACPM1 ON NACPM1.ID_NACIONALIDAD = RP1.FK_NACIONALIDAD
LEFT JOIN RCE_GED.SYS_SEXO SEXPM1 ON SEXPM1.ID_SEXO = RP1.FK_SEXO
LEFT JOIN RCE_GED.RCE_DOMICILIO DOMPM1 ON DOMPM1.ID_DOMICILIO = RP1.FK_DOMICILIO
LEFT JOIN RCE_GED.SYS_ASENTAMIENTO ASEPM1 ON ASEPM1.ID_ASENTAMIENTO = DOMPM1.FK_ASENTAMIENTO

--INFORMACION REFERIDA A MADRE/PADRE 2--
LEFT JOIN RCE_GED.RCE_REGISTRO_PERSONA RPM2 ON RPM2.FK_REGISTRO = RR.ID_REGISTRO AND RPM2.EVENTO = 'onPadre2'
LEFT JOIN RCE_GED.RCE_PERSONA RP2 ON (RPM2.FK_PERSONA = RP2.ID_PERSONA)
LEFT JOIN RCE_GED.SYS_TIPO_DOCUMENTO STD2 ON (RP2.FK_TIPO_DOCUMENTO = STD2.ID_TIPO_DOCUMENTO)
LEFT JOIN RCE_GED.SYS_NACIONALIDAD NACPM2 ON NACPM2.ID_NACIONALIDAD = RP2.FK_NACIONALIDAD
LEFT JOIN RCE_GED.SYS_SEXO SEXPM2 ON SEXPM2.ID_SEXO = RP2.FK_SEXO
LEFT JOIN RCE_GED.RCE_DOMICILIO DOMPM2 ON DOMPM2.ID_DOMICILIO = RP2.FK_DOMICILIO
LEFT JOIN RCE_GED.SYS_ASENTAMIENTO ASEP2 ON ASEP2.ID_ASENTAMIENTO = DOMPM2.FK_ASENTAMIENTO

--INFORMACION REFERIDA AL INTERVINIENTE--
LEFT JOIN RCE_GED.RCE_REGISTRO_PERSONA RPM3 ON RPM3.FK_REGISTRO = RR.ID_REGISTRO AND RPM3.EVENTO = 'onIntervinienteDefuncion'
LEFT JOIN RCE_GED.RCE_PERSONA RP3 ON (RPM3.FK_PERSONA = RP3.ID_PERSONA)
LEFT JOIN RCE_GED.SYS_TIPO_DOCUMENTO STD3 ON (RP3.FK_TIPO_DOCUMENTO = STD3.ID_TIPO_DOCUMENTO)
LEFT JOIN RCE_GED.SYS_NACIONALIDAD NACPM3 ON NACPM3.ID_NACIONALIDAD = RP3.FK_NACIONALIDAD
LEFT JOIN RCE_GED.SYS_SEXO SEXPM3 ON SEXPM3.ID_SEXO = RP3.FK_SEXO
LEFT JOIN RCE_GED.RCE_DOMICILIO DOMPM3 ON DOMPM3.ID_DOMICILIO = RP3.FK_DOMICILIO
LEFT JOIN RCE_GED.SYS_ASENTAMIENTO ASEP3 ON ASEP3.ID_ASENTAMIENTO = DOMPM3.FK_ASENTAMIENTO
LEFT JOIN RCE_GED.RCE_OBSERVACIONES OBS3 ON RPM3.FK_OBSERVACIONES = OBS3.ID_OBSERVACIONES

--INFORMACION REFERIDA AL ESTABLECIMIENTO DE SALUD--
LEFT JOIN RCE_GED.SYS_ESTABLECIMIENTO_SALUD SAL ON RD.FK_ESTABLECIMIENTO_SALUD = SAL.ID_ESTABLECIMIENTO_SALUD
LEFT JOIN RCE_GED.RCE_DOMICILIO DOMICILIO_EST ON SAL.FK_DOMICILIO = DOMICILIO_EST.ID_DOMICILIO
LEFT JOIN EU_GED.EU_PROVINCIA PROV_ESTABLECIMIENTO ON PROV_ESTABLECIMIENTO.ID = DOMICILIO_EST.FK_PROVINCIA
LEFT JOIN RCE_GED.SYS_NACIONALIDAD EST_PAIS ON EST_PAIS.ID_NACIONALIDAD = DOMICILIO_EST.FK_PAIS

--INFORMACION REFERIDA AL PERSONAL DE SALUD--
LEFT JOIN RCE_GED.RCE_MEDICO MED ON MED.ID_MEDICO = RD.FK_MEDICO
LEFT JOIN RCE_GED.SYS_PROFESIONAL_MEDICINA PROF ON PROF.ID_PROFESION = MED.FK_PROFESIONAL_MEDICINA


