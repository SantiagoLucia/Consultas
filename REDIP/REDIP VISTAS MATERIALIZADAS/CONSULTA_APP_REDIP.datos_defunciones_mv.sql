    ENF1.CODIGO AS COD_CAUSA_ORIGINARIA_DEF,
    ENF1.DESCRIPCION AS DESC_CAUSA_ORIGINARIA_DEF,
    ENF3.CODIGO AS COD_CAUSA_MEDIATA_DEF,
