        WHEN 2 THEN 
            TO_CHAR (DOMPM2.OBSERVACIONES)
        WHEN 3 THEN 
            CONCAT (DOMPM2.CALLE_ALTURA, ' - Ciudad de Buenos Aires')
        WHEN 4 THEN 
            CONCAT ( 'Nombre de la Villa_Asentamiento: ', CONCAT ( ASEP2.NOMBRE, CONCAT ( ', Manzana: ', CONCAT (DOMPM2.MANZANA, CONCAT (', Casa: ', DOMPM2.CASA)))))
        WHEN 5 THEN 
            CONCAT (DOMPM2.CALLE_ALTURA, ' - Ciudad de Buenos Aires')
        WHEN 7 THEN 
            CONCAT ( DOMPM2.MUNICIPIO, CONCAT ( ' - ', CONCAT (DOMPM2.LOCALIDAD, CONCAT (' - ', DOMPM2.CALLE_ALTURA))))
    END AS "DOMICILIO MADRE_PADRE2",
  
    STD3.DESCRIPCION AS "TIPO_DOC_DECLARANTE",
    RP3.NUMERO_DOCUMENTO AS "NRO_DOC_DECLARANTE",
    CONCAT ( RP3.PRIMER_NOMBRE, CONCAT (CONCAT (' ', RP3.SEGUNDO_NOMBRE), CONCAT (' ', RP3.TERCER_NOMBRE))) AS "NOMBRES_DECLARANTE",
    CONCAT ( RP3.PRIMER_APELLIDO, CONCAT (CONCAT (' ', RP3.SEGUNDO_APELLIDO), CONCAT (' ', RP3.TERCER_APELLIDO))) AS "APELLIDOS_DECLARANTE",
    NACPM3.DESCRIPCION_NACIONALIDAD AS "NACIONALIDAD_DECLARANTE",
    SEXPM3.DESCRIPCION AS "SEXO_DECLARANTE",
    RP3.EMAIL AS "MAIL_DECLARANTE",
    RP3.TELEFONO AS "TELEFONO_DECLARANTE",
    TO_CHAR (RP3.FECHA_NACIMIENTO, 'YYYY-MM-DD') AS "FECHA_NACIMIENTO_DECLARANTE",
    RP3.CUIL AS "CUIL_DECLARANTE",
    RP3.ESTADO_CIVIL AS "ESTADO_CIVIL_DECLARANTE",
    RP3.PROFESION AS "PROFESION_DECLARANTE",

    CASE DOMPM3.FK_TIPO_DOMICILIO
        WHEN 1 THEN 
            CONCAT ( 'Argentina, Buenos Aires, ', CONCAT ( DOMPM3.MUNICIPIO, CONCAT ( ', ', CONCAT (DOMPM3.LOCALIDAD, CONCAT (', ', DOMPM3.CALLE_ALTURA)))))
        WHEN 2 THEN 
            TO_CHAR (DOMPM3.OBSERVACIONES)
        WHEN 3 THEN 
            CONCAT (DOMPM3.CALLE_ALTURA, ' - Ciudad de Buenos Aires')
        WHEN 4 THEN 
            CONCAT ( 'Nombre de la Villa_Asentamiento: ', CONCAT ( ASEP3.NOMBRE, CONCAT ( ', Manzana: ', CONCAT (DOMPM3.MANZANA, CONCAT (', Casa: ', DOMPM3.CASA)))))
        WHEN 5 THEN 
            CONCAT (DOMPM3.CALLE_ALTURA, ' - Ciudad de Buenos Aires')
        WHEN 7 THEN 
            CONCAT ( DOMPM3.MUNICIPIO, CONCAT ( ' - ', CONCAT (DOMPM3.LOCALIDAD, CONCAT (' - ', DOMPM3.CALLE_ALTURA))))
    END AS "DOMICILIO_DECLARANTE",

    OBS3.DESCRIPCION AS ACLARACIONES_DECLARANTE,
    SAL.ID_ESTABLECIMIENTO_SALUD,
