    CONCAT ( RP2.PRIMER_APELLIDO, CONCAT (CONCAT (' ', RP2.SEGUNDO_APELLIDO), CONCAT (' ', RP2.TERCER_APELLIDO))) AS "APELLIDOS_MADRE_PADRE2",
    NACPM2.DESCRIPCION_NACIONALIDAD AS "NACIONALIDAD_MADRE_PADRE2",
    SEXPM2.DESCRIPCION AS "SEXO_MADRE_PARE2",
    RP2.EMAIL AS "MAIL_MADRE_PADRE2",
    RP2.TELEFONO AS "TELEFONO_MADRE_PADRE2",
    TO_CHAR (RP2.FECHA_NACIMIENTO, 'YYYY-MM-DD') AS "FECHA_NACIMIENTO_MADRE_PADRE2",
    RP2.CUIL AS "CUIL_MADRE_PADRE2",
    RP2.ESTADO_CIVIL AS "ESTADO_CIVIL_MADRE_PADRE2",
    RP2.PROFESION AS "PROFESION_MADRE_PADRE2",

    CASE DOMPM2.FK_TIPO_DOMICILIO
        WHEN 1 THEN 
